#include <stdint.h>
#include <stdio.h>

uint8_t key[] = { 0x8a, 0x36, 0xa6, 0x34, 0x8e, 0xe3, 0x97,
		  0x94, 0xd5, 0xb8, 0xbc, 0xca, 0xb7, 0x80 };

uint8_t a[]   = { 0xe2, 0x53, 0xca, 0x58, 0xe1, 0xbc, 0xe0,
                  0xfb, 0xa7, 0xd4, 0xd8, 0x95, 0x83, 0xb2 };

int main(void) {
	for (unsigned i = 0; i < 0xe; ++i) {
		putchar(key[i] ^ a[i]);
	}

	putchar(0xa);

	return 0;
}
